# mixing distribution 
u[i] ~ dgamma( r[ gender[i]+1 ] , r[ gender[i]+1 ]  )
}	
for (j in 1:2){
# prior distributions
lambda[j]~dgamma( 0.001, 0.001 )
r[j]~dgamma( 0.001, 0.001 )
# dispersion index
di[j] <-(1+lambda[j]/r[j])
# assumed variance
var[j] <- lambda[j]*di[j]
# negative binomial probability
p[j] <- r[j]/(r[j]+lambda[j])
}


}

"
